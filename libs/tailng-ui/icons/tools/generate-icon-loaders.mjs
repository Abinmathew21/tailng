import { readFile, writeFile } from 'node:fs/promises';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const require = createRequire(import.meta.url);
const currentDir = path.dirname(fileURLToPath(import.meta.url));

const PACKS = [
  {
    exportPrefix: 'lucide',
    packageName: '@ng-icons/lucide',
  },
];

function toKebabCase(value) {
  return value
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/_/g, '-')
    .toLowerCase();
}

function toCamelCase(value) {
  return value.replace(/-([a-z0-9])/g, (_, char) => char.toUpperCase());
}

function toPascalCase(value) {
  return value.replace(/(^[a-z])|(-[a-z0-9])/g, (match) => match.replace('-', '').toUpperCase());
}

async function resolveTypesFilePath(packageName) {
  const packageJsonPath = require.resolve(`${packageName}/package.json`);
  const packageDirectory = path.dirname(packageJsonPath);
  const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'));

  const typesRelativePath = packageJson.typings ?? packageJson.types;
  if (typeof typesRelativePath !== 'string' || typesRelativePath.trim().length === 0) {
    throw new Error(`Package "${packageName}" does not declare typings/types path.`);
  }

  return path.resolve(packageDirectory, typesRelativePath);
}

async function readPackageExportNames(packageName, exportPrefix) {
  const typesFilePath = await resolveTypesFilePath(packageName);
  const typeDeclarations = await readFile(typesFilePath, 'utf8');

  const exportNames = [];
  const declarationRegex = /^declare const ([A-Za-z0-9_]+)\s*=/gm;

  for (const match of typeDeclarations.matchAll(declarationRegex)) {
    const exportName = match[1];
    if (exportName.startsWith(exportPrefix)) {
      exportNames.push(exportName);
    }
  }

  return exportNames;
}

function buildExportNameMap(exportNames, exportPrefix) {
  const map = new Map();

  for (const exportName of exportNames) {
    const iconNameSuffix = exportName.slice(exportPrefix.length);
    if (iconNameSuffix.length === 0) {
      continue;
    }

    const kebabCaseName = toKebabCase(iconNameSuffix);
    const camelCaseName = toCamelCase(kebabCaseName);

    map.set(kebabCaseName, exportName);
    map.set(camelCaseName, exportName);
  }

  return Object.fromEntries(
    [...map.entries()].sort(([leftName], [rightName]) => leftName.localeCompare(rightName)),
  );
}

function toConstIdentifier(packageName) {
  return packageName
    .replace(/^@/, '')
    .replace(/[^a-zA-Z0-9]+/g, '_')
    .replace(/_+/g, '_')
    .toUpperCase();
}

function createGeneratedContent(packDefinitions) {
  const contentLines = [];

  contentLines.push('// This file is auto-generated by tools/generate-icon-loaders.mjs');
  contentLines.push('// Do not edit manually.');
  contentLines.push('');
  contentLines.push('type TngIconSvg = string;');
  contentLines.push('type TngIconLoader = () => Promise<TngIconSvg>;');
  contentLines.push('type TngIconPackLoaders = Readonly<Record<string, TngIconLoader>>;');
  contentLines.push('type TngIconModule = Readonly<Record<string, unknown>>;');
  contentLines.push('type TngLoadIconModule = () => Promise<TngIconModule>;');
  contentLines.push('');

  for (const definition of packDefinitions) {
    const modulePromiseIdentifier = `${definition.exportPrefix}ModulePromise`;
    const loadModuleFunctionName = `load${toPascalCase(definition.exportPrefix)}Module`;

    contentLines.push(`let ${modulePromiseIdentifier}: Promise<TngIconModule> | undefined;`);
    contentLines.push('');
    contentLines.push(`function ${loadModuleFunctionName}(): Promise<TngIconModule> {`);
    contentLines.push(
      `  ${modulePromiseIdentifier} ??= import('${definition.packageName}') as Promise<TngIconModule>;`,
    );
    contentLines.push(`  return ${modulePromiseIdentifier};`);
    contentLines.push('}');
    contentLines.push('');
  }

  contentLines.push('function createDynamicLoader(');
  contentLines.push('  loadModule: TngLoadIconModule,');
  contentLines.push('  exportName: string,');
  contentLines.push('): TngIconLoader {');
  contentLines.push('  return async (): Promise<TngIconSvg> => {');
  contentLines.push('    const iconModule = await loadModule();');
  contentLines.push('    const iconSvg = iconModule[exportName];');
  contentLines.push('');
  contentLines.push("    if (typeof iconSvg !== 'string') {");
  contentLines.push('      throw new Error(`Icon export "${exportName}" was not found.`);');
  contentLines.push('    }');
  contentLines.push('');
  contentLines.push('    return iconSvg;');
  contentLines.push('  };');
  contentLines.push('}');
  contentLines.push('');
  contentLines.push('function createPackLoaders(');
  contentLines.push('  loadModule: TngLoadIconModule,');
  contentLines.push('  exportNameMap: Readonly<Record<string, string>>,');
  contentLines.push('): TngIconPackLoaders {');
  contentLines.push('  const loaders: Record<string, TngIconLoader> = {};');
  contentLines.push('');
  contentLines.push('  for (const [iconName, exportName] of Object.entries(exportNameMap)) {');
  contentLines.push('    loaders[iconName] = createDynamicLoader(loadModule, exportName);');
  contentLines.push('  }');
  contentLines.push('');
  contentLines.push('  return Object.freeze(loaders);');
  contentLines.push('}');
  contentLines.push('');

  for (const definition of packDefinitions) {
    const identifierBase = toConstIdentifier(definition.packageName);
    const exportConstName = `${identifierBase}_EXPORT_NAME_MAP`;
    const loadModuleFunctionName = `load${toPascalCase(definition.exportPrefix)}Module`;
    const loaderConstName = `${definition.exportPrefix}PackLoaders`;

    contentLines.push(
      `const ${exportConstName}: Readonly<Record<string, string>> = Object.freeze(`,
    );
    contentLines.push(`${JSON.stringify(definition.exportNameMap, null, 2)}`);
    contentLines.push(');');
    contentLines.push('');
    contentLines.push(`export const ${loaderConstName}: TngIconPackLoaders = createPackLoaders(`);
    contentLines.push(`  ${loadModuleFunctionName},`);
    contentLines.push(`  ${exportConstName},`);
    contentLines.push(');');
    contentLines.push('');
  }

  return `${contentLines.join('\n')}\n`;
}

async function main() {
  const workspaceRoot = path.resolve(currentDir, '../../../..');
  const outputPath = path.join(
    workspaceRoot,
    'libs/tailng-ui/icons/src/lib/icon-loaders.generated.ts',
  );

  const packDefinitions = [];

  for (const packConfig of PACKS) {
    const exportNames = await readPackageExportNames(
      packConfig.packageName,
      packConfig.exportPrefix,
    );

    packDefinitions.push({
      exportNameMap: buildExportNameMap(exportNames, packConfig.exportPrefix),
      exportPrefix: packConfig.exportPrefix,
      packageName: packConfig.packageName,
    });
  }

  const generatedContent = createGeneratedContent(packDefinitions);
  await writeFile(outputPath, generatedContent, 'utf8');
}

await main();
